const arr = [-5, -7, 5, 2, 8]
function filter(arr, cb) {
  const result = []
  for (let i = 0; i < arr.length; i++) {
    const array = arr[i]
    if (cb(array)) {
      result.push(arr[i])
    }
  }
  return result;
}
function Pos(n) {
  return n >= 0
}
function Neg(n) {
  return n < 0
}
console.log(filter(arr, Pos))
console.log(filter(arr, Neg))

